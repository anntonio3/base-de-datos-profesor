    public Carreras recolectarDatos() {
        Carreras ca = new Carreras(); 
        ca.setId_carrera(Integer.parseInt(txtIdCarrera.getText()));
        ca.setNom_carrera(txtNomCarrera.getText());
        return ca;
    }
    
    private void insertarCarrera( ) {
            Carreras ca= recolectarDatos();
            CarrerasDAO carreraDAO = new CarrerasDAO();
            carreraDAO.insertarCarrera(ca);
    }


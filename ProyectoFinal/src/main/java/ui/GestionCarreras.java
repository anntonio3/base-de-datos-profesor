    private void btnEliminarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnEliminarMouseClicked
        CarrerasDAO carrerasDAO = new CarrerasDAO();

        List<Carreras> carreras = carrerasDAO.buscarCarreraPorNombre(Integer.parseInt(txtDatoBusqueda.getText()));
        if (!carreras.isEmpty()) {
            int id= Integer.parseInt(txtDatoBusqueda.getText());

            eliminarCarrera(id);
        } else {
            JOptionPane.showMessageDialog(this, "No se encontró ningúna Carrera");
        }
    }//GEN-LAST:event_btnEliminarMouseClicked


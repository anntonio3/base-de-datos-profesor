       private void cargarDatosEnTabla (){
        try {
            CarrerasDAO carrerasDAO = new CarrerasDAO();
            List<Carreras> listaCarreras = carrerasDAO.obtenerCarreras();
            
            DefaultTableModel model = (DefaultTableModel) visorCarreras.getModel();
            model.setRowCount(0); //Limpiar el modelo
            
            for(Carreras ca : listaCarreras){
                model.addRow(new Object[]{
                    ca.getId_carrera(),
                    ca.getNom_carrera()});
            }
        } catch (SQLException ex) {
            System.out.println("No");
        }
    }
       
     
    
    private void buscarCarrerasPorID(int idCarrera) {
       
            CarrerasDAO carrerasDAO = new CarrerasDAO();
            List<Carreras> listaCarreras = carrerasDAO.buscarCarreraPorNombre(idCarrera);
            
            DefaultTableModel model = (DefaultTableModel) visorCarreras.getModel();
            model.setRowCount(0); // Limpiar el modelo
            
            for (Carreras ca : listaCarreras) {
                model.addRow(new Object[]{
                    ca.getId_carrera(),
                    ca.getNom_carrera()});
            }
        
    }
    
    private void eliminarCarrera(int id) {
            CarrerasDAO carrerasDAO = new CarrerasDAO();
            carrerasDAO.eliminarCarrera(id);
    } 
    
    



    private void btnActualizarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnActualizarMouseClicked
        CarrerasDAO carrerasDAO = new CarrerasDAO();

        List<Carreras> carreras = carrerasDAO.buscarCarreraPorNombre(Integer.parseInt(txtDatoBusqueda.getText()));
        if (!carreras.isEmpty()) {
            ac.setLocationRelativeTo(null);
            ac.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(this, "No se encontró ningúna Carrera");
        }
        
    }//GEN-LAST:event_btnActualizarMouseClicked

    private void btnBuscarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnBuscarMouseClicked
        buscarCarrerasPorID(Integer.parseInt(txtDatoBusqueda.getText()));
        CarrerasDAO carrerasDAO = new CarrerasDAO();

        List<Carreras> carreras = carrerasDAO.buscarCarreraPorNombre(Integer.parseInt(txtDatoBusqueda.getText()));
        if (!carreras.isEmpty()) {
            Carreras ca = carreras.get(0); 
            ac.mostrarDatos(ca);
        } else {
            JOptionPane.showMessageDialog(this, "No se encontró ningúna Carrera");
        }
        
    }//GEN-LAST:event_btnBuscarMouseClicked


    public Datos_laborales recolectarDatos() {
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("yyyy-MM-dd");
        
        Datos_laborales datosL = new Datos_laborales(); 
        datosL.setNo_profesor(Integer.parseInt(txtNoProfesor.getText()));
        datosL.setNombramiento_institucional(txtNombramientoI.getText());
        datosL.setDedicacion(txtDedicacion.getText());
        datosL.setDependencia(txtDependencia.getText());
        datosL.setInicio_contrato(LocalDate.parse(txtInicioContrato.getText(), formatter));
        datosL.setFin_contrato(LocalDate.parse(txtFinContrato.getText(), formatter));
        datosL.setEstado(txtEstado.getText());
        datosL.setEmail_institucional(txtEmailI.getText());

        return datosL;
    }
    
    public void mostrarDatos(Datos_laborales datosL) {
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("yyyy-MM-dd");
        
        txtNoProfesor.setText(String.valueOf(datosL.getNo_profesor()));
        txtNombramientoI.setText(datosL.getNombramiento_institucional());
        txtDedicacion.setText(datosL.getDedicacion());
        txtDependencia.setText(datosL.getDependencia());
        txtInicioContrato.setText(datosL.getInicio_contrato().format(formatter));
        txtFinContrato.setText(datosL.getFin_contrato().format(formatter));
        txtEstado.setText(datosL.getEstado());
        txtEmailI.setText(datosL.getEmail_institucional());

    }
    
    private void actualizarDatosLaborales( ) {
            Datos_laborales datosL= recolectarDatos();
            Datos_laboralesDAO datosLDAO = new Datos_laboralesDAO();
            datosLDAO.actualizarDatosLaborales(datosL);
    }


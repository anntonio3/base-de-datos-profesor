       private void cargarDatosEnTabla (){
        try {
            AsignaturasDAO asigDAO = new AsignaturasDAO();
            List<Asignaturas> listaAsignaturas = asigDAO.obtenerAsignaturas();
            
            DefaultTableModel model = (DefaultTableModel) visorAsignaturas.getModel();
            model.setRowCount(0); //Limpiar el modelo
            
            for(Asignaturas asig : listaAsignaturas){
                model.addRow(new Object[]{
                    asig.getNombre_curso(),
                    asig.getDES(),
                    asig.getNivel(),
                    asig.getPrograma_educativo(),
                    asig.getFecha_inicio_curso(),
                    asig.getNo_alumnos_inscritos(),
                    asig.getNo_duracion_semanas(),
                    asig.getProfesor_CURP()});
            }
        } catch (SQLException ex) {
            System.out.println("No");
        }
    }
       
     
    
    private void buscarAsignaturasPorID(String nomCurso) {
       
            AsignaturasDAO asigDAO = new AsignaturasDAO();
            List<Asignaturas> listaAsig = asigDAO.buscarAsignaturasPorNombre(nomCurso);
            
            DefaultTableModel model = (DefaultTableModel) visorAsignaturas.getModel();
            model.setRowCount(0); // Limpiar el modelo
            
            for (Asignaturas asig : listaAsig) {
                model.addRow(new Object[]{
                    asig.getNombre_curso(),
                    asig.getDES(),
                    asig.getNivel(),
                    asig.getPrograma_educativo(),
                    asig.getFecha_inicio_curso(),
                    asig.getNo_alumnos_inscritos(),
                    asig.getNo_duracion_semanas(),
                    asig.getProfesor_CURP()});
            }
        
    }
    
    private void eliminarAsignaturas(String nomCurso) {
            AsignaturasDAO asigDAO = new AsignaturasDAO();
            asigDAO.eliminarAsignaturas(nomCurso);
    }
    
    



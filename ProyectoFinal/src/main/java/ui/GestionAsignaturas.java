    private void btnActualizarProfesorMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnActualizarProfesorMouseClicked
 
        AsignaturasDAO asigDAO = new AsignaturasDAO();

        List<Asignaturas> asig = asigDAO.buscarAsignaturasPorNombre(txtDatoBusqueda.getText());
        if (!asig.isEmpty()) {
            aa.setLocationRelativeTo(null);
            aa.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(this, "No se encontró ningúna Asignatura");
        }
    }//GEN-LAST:event_btnActualizarProfesorMouseClicked

    private void btnBuscarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnBuscarMouseClicked
        buscarAsignaturasPorID(txtDatoBusqueda.getText());
        
        AsignaturasDAO asigDAO = new AsignaturasDAO();

        List<Asignaturas> asig = asigDAO.buscarAsignaturasPorNombre(txtDatoBusqueda.getText());
        if (!asig.isEmpty()) {
            Asignaturas a = asig.get(0); 
            aa.mostrarDatos(a);
        } else {
            JOptionPane.showMessageDialog(this, "No se encontró ningúna Asignatura");
        }
    }//GEN-LAST:event_btnBuscarMouseClicked


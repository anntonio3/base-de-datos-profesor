    private void btnEliminarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnEliminarMouseClicked
        AsignaturasDAO asigDAO = new AsignaturasDAO();

        List<Asignaturas> asig = asigDAO.buscarAsignaturasPorNombre(txtDatoBusqueda.getText());
        if (!asig.isEmpty()) {
            String id=txtDatoBusqueda.getText();
            eliminarAsignaturas(id);
        } else {
            JOptionPane.showMessageDialog(this, "No se encontró ningúna Asignatura");
        }
    }//GEN-LAST:event_btnEliminarMouseClicked

